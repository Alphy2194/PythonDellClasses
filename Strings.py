# string is a collection of words
course = "WEB"
text = "Hello World"
print(text)

# printing index positions/ accessing an element in a string
print(text[0])
print(text[2])  # printing the first letter 'l'

# size of a string
print(len(text))

# modifying a string
print(text.upper())  # converting it to upper case
print(text.lower())  # converting it to lower case
print(course.lower())

# string concatenation - combining strings
first = "Hello"
second = "there"

print(first + " " + second)
print(first,second)