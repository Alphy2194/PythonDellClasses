# while loop - print numbers within a given range

# incrementing values
number = 20
while number <= 25:
    print(number)
    number += 1

# decrementing values
a = 10
while a >= 1:
    print(a)
    a -= 1

# for loop
languages = ["Python", "PHP", "Java", "Kotlin", "C++"]
for x in languages:
    print(x)

# Range
for x in range(10):
    print(x)

for b in range(15,20):
    print(b)


# incrementing values with a specific range
for c in range(4, 20, 3):
    print(c)