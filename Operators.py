# Arithmetic operators
x = 3
y = 4

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)  # modulus - returns the remainder after dividing the two numbers

# Comparison operators
num1 = 78
num2 = 45

print(num1 < num2)
print(num1 > num2)
print(num1 >= num2)
print(num1 <= num2)
print(num1 == num2)  # Equal to
print(num1 != num2)  # Not equal to


# Assignment operators - they assign values to variables
a = 7
print(a)

b = 13
b += 2
print(b)

c = 14
c -= 2
print(c)

# logical operators - AND, OR, NOT
number1 = 8
number2 = 20
print(number1 > number2 and number2 > number1)
print(number1 > number2 or number2 > number1)
print(not(number1 > number2 and number2 > number1))
print(not(number1 > number2 or number2 > number1))

